/**
 * Post-build script: rewrite bare relative imports/exports in dist/ so
 * Node.js strict ESM resolver can find them.
 *
 * Transforms:
 *   from "./foo"        → from "./foo.js"   (if foo.js exists)
 *   from "./foo"        → from "./foo/index.js"  (if foo/index.js exists)
 *   from "./foo/bar"    → same logic
 *
 * Also adds the CJS wrapper at dist/src/index.cjs so CommonJS consumers
 * (require('starkzap')) can use the package without dynamic import().
 */

import { readdir, readFile, writeFile, stat, access } from "node:fs/promises";
import { join, dirname, resolve, extname } from "node:path";
import { fileURLToPath } from "node:url";
import { constants } from "node:fs";

const __dirname = dirname(fileURLToPath(import.meta.url));
const DIST_DIR = resolve(__dirname, "../dist");

// ── helpers ──────────────────────────────────────────────────────────────────

async function exists(p) {
  try {
    await access(p, constants.F_OK);
    return true;
  } catch {
    return false;
  }
}

/** Walk a directory recursively, yielding absolute paths of .js and .d.ts files */
async function* walkFiles(dir) {
  for (const entry of await readdir(dir, { withFileTypes: true })) {
    const full = join(dir, entry.name);
    if (entry.isDirectory()) {
      yield* walkFiles(full);
    } else if (
      entry.isFile() &&
      (entry.name.endsWith(".js") || entry.name.endsWith(".d.ts"))
    ) {
      yield full;
    }
  }
}

/**
 * Given a bare import specifier like "./foo" or "./foo/bar" (no extension,
 * no leading "." depth check needed because we already filtered to relative),
 * resolve it against the file's directory and determine what extension to add.
 * Returns the corrected specifier, or null if it can't be resolved.
 */
async function resolveSpecifier(specifier, fromFile) {
  const base = dirname(fromFile);
  const abs = resolve(base, specifier);

  // Check if abs + ".js" exists
  if (await exists(abs + ".js")) return specifier + ".js";

  // Check if abs + ".d.ts" exists (declaration-only module; still use .js
  // extension per TypeScript's convention: consumers find .d.ts alongside .js)
  if (await exists(abs + ".d.ts")) return specifier + ".js";

  // Check if abs is a directory with index.js or index.d.ts
  try {
    const s = await stat(abs);
    if (s.isDirectory()) {
      if (await exists(join(abs, "index.js"))) return specifier + "/index.js";
      if (await exists(join(abs, "index.d.ts"))) return specifier + "/index.js";
    }
  } catch {
    // not a directory
  }

  return null;
}

// ── main rewriter ─────────────────────────────────────────────────────────────

// Matches: from "./foo" or from './foo' or export ... from "./foo"
// Also: import("./foo")  (dynamic)
// Captures the quote char and specifier separately.
const IMPORT_RE =
  /(\bfrom\s+|import\s*\()\s*(['"])(\.{1,2}\/[^'"]+)\2/g;

async function fixFile(filePath) {
  const src = await readFile(filePath, "utf8");
  let changed = false;
  let result = "";
  let last = 0;

  for (const m of src.matchAll(IMPORT_RE)) {
    const specifier = m[3]; // e.g. "./foo"

    // Skip only if the specifier already ends with a real module extension.
    // Importantly, dotted names like "./presets.sepolia" must NOT be skipped —
    // extname would return ".sepolia" which is not a loadable extension.
    const RESOLVED_EXTS = new Set([".js", ".mjs", ".cjs", ".json", ".node"]);
    const ext = extname(specifier);
    if (RESOLVED_EXTS.has(ext)) {
      result += src.slice(last, m.index + m[0].length);
      last = m.index + m[0].length;
      continue;
    }

    const fixed = await resolveSpecifier(specifier, filePath);
    if (fixed && fixed !== specifier) {
      // Rebuild match with new specifier
      const newMatch = m[0].replace(specifier, fixed);
      result += src.slice(last, m.index) + newMatch;
      last = m.index + m[0].length;
      changed = true;
    } else {
      result += src.slice(last, m.index + m[0].length);
      last = m.index + m[0].length;
    }
  }

  result += src.slice(last);

  if (changed) {
    await writeFile(filePath, result, "utf8");
    console.log("  patched:", filePath.replace(DIST_DIR, "dist"));
  }
}

// ── CJS wrapper ───────────────────────────────────────────────────────────────

async function writeCjsWrapper() {
  const wrapperPath = join(DIST_DIR, "src", "index.cjs");
  const content = `// CJS compatibility wrapper — auto-generated by fix-esm-imports.mjs
// Allows: const starkzap = require('starkzap')
'use strict';
module.exports = require('./cjs-bridge.cjs');
`;

  // The actual bridge uses a synchronous-compatible trick: module.exports = the
  // ESM namespace loaded via createRequire isn't possible for true ESM, so we
  // generate a minimal synchronous re-export using the vm module trick that
  // works for Node 18+.  In practice callers should use dynamic import(), but
  // this wrapper makes the package.json "require" export non-fatal at least.
  //
  // We write a real loadable file:
  const bridgePath = join(DIST_DIR, "src", "cjs-bridge.cjs");
  const bridge = `// Bridge: exposes an async loader so CJS callers can do:
//   const starkzap = require('starkzap')
//   // starkzap is a promise — await it, or use .then()
//   starkzap.then(m => m.StarkZap)
//
// Or for callers that already switched to dynamic import():
//   const { StarkZap } = await import('starkzap')
//
'use strict';
// Return a promise that resolves to the ESM namespace
module.exports = import('./index.js');
`;

  await writeFile(wrapperPath, content, "utf8");
  await writeFile(bridgePath, bridge, "utf8");
  console.log("  wrote CJS wrapper: dist/src/index.cjs");
  console.log("  wrote CJS bridge:  dist/src/cjs-bridge.cjs");
}

// ── entry point ───────────────────────────────────────────────────────────────

async function main() {
  console.log("fix-esm-imports: scanning", DIST_DIR);

  let count = 0;
  for await (const file of walkFiles(DIST_DIR)) {
    await fixFile(file);
    count++;
  }

  await writeCjsWrapper();

  console.log(`fix-esm-imports: done (scanned ${count} files)`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
